  <h2>Mission:</h2><p>The mission of the myFOSSIL eMuseum is to advance scientific understanding by digitally repositing personal specimens to be used in scientific research. These are created through collaboration between professional and avocational paleontologists, educators, and many more!</p>
  <h2>Vision:</h2><p>Transform the idea of a museum collection to include digital vouchers and provide a platform for scientific collaboration.</p>
  <h2>Curation:</h2><p>Uploaded specimens will undergo curation to determine the quality of information. Specimens will only be rejected if they are not fossil material (e.g., rocks, archaeological artifacts, any other non-fossil organism). Specimen information will be examined by our curation team to ensure the data entered are correct.</p>
  <p>Curation will result in certain specimens being recognized as research grade. Research specimens will have detailed information about the organism that may benefit the larger scientific community and will be sent to biodiversity data aggregators (e.g., <a ahref="https://www.idigbio.org/" target="_blank">iDigBio</a>, <a ahref="https://www.gbif.org/" target="_blank">GBIF</a>). Specimens not deemed research grade will remain a valuable part of the eMuseum for education and exhibit purposes. With additional information, these specimens may be elevated to research grade.</p>
  <h2>What materials do we accept?</h2><p>All fossil material will be accepted into the eMuseum. You can include some information about your specimen, but it is not required that you fill out all of the information fields. The more information you enter, the more likely it will be a research grade specimen record. If the specimen you have uploaded is not a fossil, it will not be placed into the eMuseum.</p>
